import java.util.*;
public class operators{
    public static void main(String args[]){

        /*
        -> BINARY 

        int a=10;
        int b=5;
        System.out.println("Operation result:" + (a+b));
        */

       /*
       -> PRE-INCREMENT UNARY

       int a =10;
       int b =++a;
       System.out.println(a);
       System.out.println(b);
       */

      /*
      -> POST-INCREMENT UNARY

      int a =10;
      int b =a++;
      System.out.println(a);
      System.out.println(b);
      */


     /*
     -> DECREMENT(PRE AND POST)

      int a =10;
      int b =a--;   //try --a;
      System.out.println(a);
      System.out.println(b);
      */

     /*
    ->  RELATIONAL OPERATOR

       int a =10;
       int b =5;
       System.out.println(a==b);
       System.out.println(a!=b);
       System.out.println(a>b);
       System.out.println(a<b);
       System.out.println(a>=b);
       System.out.println(a<=b);
       */

      
   /*
   -> LOGICAL OPERATORS (&.||,!)

    System.out.println((3<2)&&(5>0));
    System.out.println((3<2)||(5>0));
    System.out.println(!(3<2));
   */


  int a=10;
  //a=a+10;  same as a+=10
  a+=10;
  System.out.println(a);

  int b=5;   
  //b=b-5;   same as b-=5
  b-=5;
  System.out.println(b);



    








    }
}